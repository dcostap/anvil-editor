$ErrorActionPreference = "Stop"

function Invoke-Msys([string]$Command) {
  $bash = "C:\msys64\usr\bin\bash.exe"
  if (!(Test-Path $bash)) { throw "MSYS2 bash not found: $bash" }
  & $bash -lc "export MSYSTEM=MINGW64; export PATH=/mingw64/bin:/usr/bin:/bin:`$PATH; $Command"
  if ($LASTEXITCODE -ne 0) { throw "MSYS command failed with exit code $LASTEXITCODE" }
}

function Prepare-RunDirectory([string]$RepoRoot) {
  $repoMsys = $RepoRoot -replace '^([A-Za-z]):', '/$1' -replace '\\', '/'
  $repoMsys = $repoMsys.ToLowerInvariant()
  Invoke-Msys @"
cd '$repoMsys' && \
meson compile -C build-windows-x86_64 && \
rm -rf .run && \
mkdir -p .run/bin .run/share/anvil .run/share/anvil/core && \
cp build-windows-x86_64/src/anvil.exe .run/bin/ && \
for module_name in core compat plugins colors fonts; do cp -r data/`$module_name .run/share/anvil/; done && \
if [ -d subprojects/widget ]; then cp -a subprojects/widget .run/share/anvil/; fi && \
if [ -d subprojects/colors ]; then cp -a subprojects/colors/colors .run/share/anvil/colors; fi && \
if [ -d subprojects/plugins ]; then cp -a subprojects/plugins/plugins/language_* .run/share/anvil/plugins/ 2>/dev/null || true; fi && \
cp build-windows-x86_64/start.lua .run/share/anvil/core/
"@
}

function New-ScrollFixture([string]$TestProject) {
  New-Item -ItemType Directory -Force -Path $TestProject | Out-Null
  $fixture = Join-Path $TestProject "render_scroll_fixture.txt"
  $lines = New-Object System.Collections.Generic.List[string]
  $lines.Add("Anvil D3D11 renderer smoke fixture")
  $lines.Add("Generated by scripts/d3d11-smoke-test.ps1")
  $lines.Add("")
  for ($i = 1; $i -le 3000; $i++) {
    $prefix = "{0:D4}" -f $i
    $text = "function render_test_${prefix}() -- tabs`tunicode OK punctuation []{}<> long-long-long-long-long-long-long-long-long-long"
    $lines.Add("${prefix}  $text")
  }
  Set-Content -LiteralPath $fixture -Value $lines -Encoding UTF8
  return $fixture
}

